pub fn is_transparent(block_name: &str) -> bool {
    TRANSPARENT_BLOCK_PATTERNS.contains(&block_name)
}

pub fn get_emitted_light_level(block_name: &str) -> u8 {
    LIGHT_EMITTING_BLOCKS
        .iter()
        .find(|(name, _)| name == &block_name)
        .map(|(_, level)| *level)
        .unwrap_or_default()
}
